public class Operators {
    public static void main(String[] args) {
        int a = 10;
        int a1 = a++;
        int b = 10;
        int b1 = ++b;

        System.out.println("a = "+a);
        System.out.println("a1 = "+a1);
        System.out.println("-----------------");
        System.out.println("b = "+b);
        System.out.println("b1 = "+b1);
    }
}
